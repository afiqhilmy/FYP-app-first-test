model_milp = LpProblem("EV_Charging_Scheduling", LpMinimize)

stations = final_merged_df.index.tolist()

x = LpVariable.dicts(
    "schedule",
    (stations, time_slots),
    cat=LpBinary
)

model_milp += lpSum(
    final_merged_df.loc[s, "predicted_demand"] * x[s][t]
    for s in stations
    for t in time_slots
)

for s in stations:
    for t in time_slots:
        model_milp += (
            final_merged_df.loc[s, "predicted_demand"] * x[s][t]
            <= final_merged_df.loc[s, "Total Station Capacity (KW)"]
        )

GRID_LIMIT = final_merged_df["Total Station Capacity (KW)"].sum() * 0.6

for t in time_slots:
    model_milp += lpSum(
        final_merged_df.loc[s, "predicted_demand"] * x[s][t]
        for s in stations
    ) <= GRID_LIMIT

for s in stations:
    model_milp += lpSum(
        x[s][t] for t in time_slots
    ) >= 1

model_milp.solve()

