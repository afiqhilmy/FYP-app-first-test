URBAN_KEYWORDS = [
    "city", "mall", "bandar", "pusat", "square", "komersial",
    "commercial", "downtown", "indera mahkota", "kuantan city"
]

def is_urban(address):
    address = address.lower()
    return any(keyword in address for keyword in URBAN_KEYWORDS)

# Peak hour flag generator
# -----------------------------
def peak_hour(row):
    urban = is_urban(row["Station Address"])
    pop = row["population"]
    adt = row["ADT"]  # or use real ADT if available

    if urban and (pop >= 60 or adt >= 30000):
        return "Yes"
    else:
        return "No"

final_data["Peak_Hour"] = final_data.apply(peak_hour, axis=1)
