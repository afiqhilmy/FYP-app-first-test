"""**Calculating cost and revenue**

7kWh/22kWh AC Charging: RM0.55/kWh

180kWh DC Charging: RM1.00/kWh

350kWh DC Charging: RM1.20/kWh

source: https://soyacincau.com/2022/12/02/ev-dc-fast-charging-in-malaysia-gentari-kwh-vs-time-based-charging-which-is-cheaper/#:~:text=350kWh%20DC%20Charging:%20RM1.&text=The%20pricing%20for%20DC%20fast,to%20charge%20at%20these%20stations.
"""

# Define tariff rates
ac_rate = 0.55
dc_rate_180_kwh = 1.00 # For 180kWh DC Charging
dc_rate_350_kwh = 1.20 # For 350kWh DC Charging

# Calculate AC Revenue
# Using 'Avg_AC_Hours' from the ev DataFrame
ev['AC_revenue (RM)'] = (ev['AC_Capacity_kW'] * ev['Avg_AC_Hours'] * ac_rate).round(2)

# Calculate DC Revenue based on tiers
def calculate_dc_revenue_tiered(row):
    total_dc_capacity = row['DC_Capacity_kW']
    avg_dc_charging_hours = row['Avg_DC_Hours'] # Use Avg_DC_Hours

    if total_dc_capacity == 0:
        return 0.0
    # Apply RM1.00/kWh for DC capacity up to 180kW
    elif total_dc_capacity <= 180:
        return total_dc_capacity * avg_dc_charging_hours * dc_rate_180_kwh
    # Apply RM1.20/kWh for DC capacity greater than 180kW
    else:
        return total_dc_capacity * avg_dc_charging_hours * dc_rate_350_kwh

ev['DC_revenue (RM)'] = ev.apply(calculate_dc_revenue_tiered, axis=1).round(2)

