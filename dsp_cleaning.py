import re
import pandas as pd

def extract_ac_capacity(text):
    if pd.isna(text):
        return 0
    ac_total_capacity = 0
    # Regex to find (power) followed by 'KW AC', 'KWAC', 'AC' AND (number of units)
    pattern = r'(\d+\.?\d*)\s*KW?\s*AC(?::\s*(\d+)\s*UNIT(?:S)?)?'
    matches = re.findall(pattern, str(text), re.IGNORECASE)
    for match in matches:
        power = float(match[0])
        units_str = match[1] # Number of units, can be None if not found
        units = int(units_str) if units_str else 1 # Default to 1 unit if not specified
        ac_total_capacity += power * units
    return int(ac_total_capacity)

def extract_dc_capacity(text):
    if pd.isna(text):
        return 0
    dc_total_capacity = 0
    # Regex to find (power) followed by 'KW DC', 'KWDC', 'DC' AND (number of units)
    pattern = r'(\d+\.?\d*)\s*KW?\s*DC(?::\s*(\d+)\s*UNIT(?:S)?)?'
    matches = re.findall(pattern, str(text), re.IGNORECASE)
    for match in matches:
        power = float(match[0])
        units_str = match[1] # Number of units, can be None if not found
        units = int(units_str) if units_str else 1 # Default to 1 unit if not specified
        dc_total_capacity += power * units
    return int(dc_total_capacity)

